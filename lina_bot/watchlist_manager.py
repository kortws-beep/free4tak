def get_watchlist_stocks(priority: int = 2) -> set:
    """
    priority 이하 관심주 전체 종목명 set 반환
    swing_master.py 의 촉매 체크에서 교차 확인용
    """
    if not os.path.exists(DB_PATH):
        return set()
    conn   = sqlite3.connect(DB_PATH)
    cursor = conn.cursor()
    cursor.execute("""
        SELECT DISTINCT stock_name FROM watchlist
        WHERE priority <= ?
    """, (priority,))
    stocks = {r[0] for r in cursor.fetchall()}
    conn.close()
    return stocks


def get_watchlist_themes(stock_name: str) -> list:
    """종목명으로 테마 조회"""
    if not os.path.exists(DB_PATH):
        return []
    conn   = sqlite3.connect(DB_PATH)
    cursor = conn.cursor()
    cursor.execute("""
        SELECT theme_name, note FROM watchlist
        WHERE stock_name LIKE ?
    """, (f"%{stock_name}%",))
    rows = cursor.fetchall()
    conn.close()
    return rows


