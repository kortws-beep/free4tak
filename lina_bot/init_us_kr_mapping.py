def init_mapping():
    conn   = sqlite3.connect(DB_PATH)
    cursor = conn.cursor()

    # 테이블 없으면 생성
    cursor.execute("""
        CREATE TABLE IF NOT EXISTS us_kr_mapping (
            id INTEGER PRIMARY KEY AUTOINCREMENT,
            us_ticker TEXT NOT NULL,
            us_name   TEXT NOT NULL,
            kr_name   TEXT NOT NULL,
            reason    TEXT,
            is_static INTEGER DEFAULT 1,
            updated_at DATETIME DEFAULT CURRENT_TIMESTAMP
        )
    """)

    added = 0
    skipped = 0
    for ticker, us_name, kr_name, reason in MAPPING_DATA:
        # 중복 방지
        cursor.execute("""
            SELECT COUNT(*) FROM us_kr_mapping
            WHERE us_ticker = ? AND kr_name = ?
        """, (ticker, kr_name))
        if cursor.fetchone()[0] > 0:
            skipped += 1
            continue

        cursor.execute("""
            INSERT INTO us_kr_mapping (us_ticker, us_name, kr_name, reason, is_static)
            VALUES (?, ?, ?, ?, 1)
        """, (ticker, us_name, kr_name, reason))
        added += 1

    conn.commit()

    # 결과 출력
    print(f"\n✅ 맵핑 초기 세트 등록 완료!")
    print(f"   추가: {added}건 / 중복 스킵: {skipped}건\n")

    # 티커별 요약
    cursor.execute("""
        SELECT us_ticker, us_name, COUNT(*) as cnt
        FROM us_kr_mapping
        GROUP BY us_ticker
        ORDER BY us_ticker
    """)
    rows = cursor.fetchall()
    print(f"{'티커':<8} {'이름':<16} {'매핑 수'}")
    print("-" * 36)
    for ticker, name, cnt in rows:
        print(f"{ticker:<8} {name:<16} {cnt}개")

    conn.close()


if __name__ == "__main__":
    init_mapping()
