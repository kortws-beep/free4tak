    added = skipped = 0
    for theme, stock, note, priority in WATCHLIST_DATA:
        cursor.execute("""
            SELECT COUNT(*) FROM watchlist
            WHERE theme_name = ? AND stock_name = ?
        """, (theme, stock))
        if cursor.fetchone()[0] > 0:
            skipped += 1
            continue
        cursor.execute("""
            INSERT INTO watchlist (theme_name, stock_name, note, priority)
            VALUES (?, ?, ?, ?)
        """, (theme, stock, note, priority))
        added += 1
    conn.commit()

    # 요약
    cursor.execute("SELECT COUNT(DISTINCT theme_name) FROM watchlist")
    total_themes = cursor.fetchone()[0]
    cursor.execute("SELECT COUNT(*) FROM watchlist")
    total_stocks = cursor.fetchone()[0]
    conn.close()

    print(f"\n✅ 관심주 DB 초기 등록 완료!")
    print(f"   추가: {added}건 / 중복 스킵: {skipped}건")
    print(f"   총 테마: {total_themes}개 / 총 종목: {total_stocks}개\n")


